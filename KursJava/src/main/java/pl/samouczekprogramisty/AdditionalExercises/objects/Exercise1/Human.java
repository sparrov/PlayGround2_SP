package pl.samouczekprogramisty.AdditionalExercises.objects.Exercise1;

public class Human {
    private int age;
    private double weight;
    private int height;
    private String name;
    private boolean isMale;

    public int getAge() {
        return age;
    }

    public double getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public String getName() {
        return name;
    }

    public boolean isMale() {
        return isMale;
    }

}
